function renderTweets(tweets) {
  for (let id of tweets) {
    $("#tweets").append(createTweetElement(id));
  }
}

// This object takes the html from the index.html in string format and applies key:value pairs from the obj tweetData to variable article
// It returns the article
function createTweetElement(obj) {
  let article = `
  <article class="tweet">
        <header>
          <img src=${obj.user.avatars.small}>
          <h1>${obj.user.name}</h1>
          <h2>${obj.user.handle}</h2>
        </header>
        <div class="tweet-content">
          <p>
            ${obj.content.text}
          </p>
        <footer>
          <p id="yo">
            ${obj.created_at}
          </p>
          <div class="animation">
            <i class="fa fa-flag" aria-hidden="true"></i>
            <i class="fa fa-retweet" aria-hidden="true"></i>
            <i class="fa fa-heart" aria-hidden="true"></i>
        </div>
       </footer>
   </article>
`
  let $tweet = $(article)
  return $tweet;
}

